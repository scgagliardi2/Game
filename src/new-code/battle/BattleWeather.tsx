export let WEATHER_RAIN_TEMPORARY = () => (1 << 0)
export let WEATHER_RAIN_DOWNPOUR = () => (1 << 1)  // unused
export let WEATHER_RAIN_PERMANENT = () => (1 << 2)
export let WEATHER_RAIN_ANY = () => (WEATHER_RAIN_TEMPORARY() | WEATHER_RAIN_DOWNPOUR() | WEATHER_RAIN_PERMANENT())
export let WEATHER_SANDSTORM_TEMPORARY = () => (1 << 3)
export let WEATHER_SANDSTORM_PERMANENT = () => (1 << 4)
export let WEATHER_SANDSTORM_ANY = () => (WEATHER_SANDSTORM_TEMPORARY() | WEATHER_SANDSTORM_PERMANENT())
export let WEATHER_SUN_TEMPORARY = () => (1 << 5)
export let WEATHER_SUN_PERMANENT = () => (1 << 6)
export let WEATHER_SUN_ANY = () => (WEATHER_SUN_TEMPORARY() | WEATHER_SUN_PERMANENT())
export let WEATHER_HAIL = () => (1 << 7)
export let WEATHER_HAIL_ANY = () => (WEATHER_HAIL())
export let WEATHER_ANY = () => (WEATHER_RAIN_ANY() | WEATHER_SANDSTORM_ANY() | WEATHER_SUN_ANY() | WEATHER_HAIL_ANY())